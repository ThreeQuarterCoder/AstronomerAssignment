
class Widget:

    def __init__(self):
        self.name = ""
        self.code = ""
        self.price = ""

    def getName(self):
        return self.name

    def getCode(self):
        return self.code

    def getPrice(self):
        return self.price

    def setName(self, name):
        self.name = name

    def setCode(self, code):
        self.code = code

    def setPrice(self, price):
        self.price = price

